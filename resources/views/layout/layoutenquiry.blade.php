                document.getElementById('resume');

            var filePath = fileInput.value;

            // Allowing file type
            var allowedExtensions =
                /(\.pdf|\.docx)$/i;

            if (!allowedExtensions.exec(filePath)) {
                $(".invalid-feedback-file").css('display', 'block');
                fileInput.value = '';
                return false;
            }
